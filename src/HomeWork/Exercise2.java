package HomeWork;

public class Exercise2 {
    public static void main(String[] args) {
        //The Math.max(x,y) method can be used to find the highest value of x and y
        System.out.println(Math.max(5,10));

        System.out.println(Math.min(10,5));//The Math.min(x,y) method can be used to find the lowest value of x and y

        System.out.println(Math.sqrt (100));//The Math.sqrt(x) method returns the square root of x

        System.out.println(Math.abs (-5.3));// The Math.abs(x) method returns the absolute (positive) value of x

        //boolean
        // returns true, because 10 is higher than 5
        System.out.println(10>5);

        int x=10;
        System.out.println(x==10);  //returns true, because the value of x is equal to 10

        System.out.println(10==15); //returns false, because 10 is not equal to 15

        int myAge=25;
        int votingAge=18;
        System.out.println(myAge >=votingAge);



    }
}
