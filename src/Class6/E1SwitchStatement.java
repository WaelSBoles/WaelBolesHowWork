package Class6;

public class E1SwitchStatement {
    public static void main(String[] args) {

        int year=2023;

        switch (year){
            case 2020:
                System.out.println("it's 2020");
                break;
            case 2021:
                System.out.println("it's 2021");
                break;
            case 2022:
                System.out.println("its'2023");
                break;
            case 2023:
                System.out.println("it'2023");
                break;
            default:
                System.out.println("it non of the above");

        }
    }
}
